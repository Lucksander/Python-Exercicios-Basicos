# Solicita ao usuário que digite o primeiro número
# O input() recebe o valor como texto, então usamos int()
# para converter o valor para número inteiro
num1 = int(input("Digite o primeiro número: "))

# Solicita ao usuário que digite o segundo número
num2 = int(input("Digite o segundo número: "))

# Realiza a soma dos dois números
soma = num1 + num2

# Realiza a subtração do primeiro número pelo segundo
subtracao = num1 - num2

# Realiza a multiplicação dos dois números
multiplicacao = num1 * num2

# Realiza a divisão do primeiro número pelo segundo
divisao = num1 / num2

# Exibe o resultado da soma
print("A soma é:", soma)

# Exibe o resultado da subtração
print("A subtração é:", subtracao)

# Exibe o resultado da multiplicação
print("A multiplicação é:", multiplicacao)

# Exibe o resultado da divisão
print("A divisão é:", divisao)